async def initialize_locations():
    """Seed regions and mosques data on startup if not already present."""
    if not db_manager.async_session_maker:
        logger.warning("Database not initialized, skipping location seeding")
        return

    try:
        async with db_manager.async_session_maker() as db:
            # Check if regions already exist
            count_query = select(Regions)
            count_result = await db.execute(count_query)
            existing_regions = count_result.scalars().all()

            if len(existing_regions) > 0:
                logger.info(f"Locations already seeded ({len(existing_regions)} regions found), skipping")
                return

            logger.info("Seeding regions and mosques data...")
            now = datetime.now(timezone.utc)
            created_regions = 0
            created_mosques = 0

            for region_name, mosque_names in SEED_DATA.items():
                region = Regions(name=region_name, created_at=now)
                db.add(region)
                await db.flush()
                created_regions += 1

                for mosque_name in mosque_names:
                    mosque = Mosques(
                        name=mosque_name,
                        region_id=region.id,
                        created_at=now,
                    )
                    db.add(mosque)
                    created_mosques += 1

            await db.commit()
            logger.info(f"Seeded {created_regions} regions and {created_mosques} mosques successfully")
    except Exception as e:
        logger.error(f"Error seeding locations: {e}", exc_info=True)